import matplotlib.pyplot as plt

def doffy(dmgList):
    asd = []
    for i in range(len(dmgList)):
        i += 1
        asd.append(i)
    plt.bar(asd, dmgList)
    total = str(sum(dmgList))
    li = str(dmgList)
    plt.figtext(.02, .02, "Total dmg = " + total +"\n"+ li)
    plt.show()

